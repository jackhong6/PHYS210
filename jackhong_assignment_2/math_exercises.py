import math

# Calculate and print the volume of a sphere with radius 3.
radius = 3
volume = 4 / 3 * math.pi * (radius ** 3)
print("The volume of a sphere with radius 3 is " + str(volume) + ".\n")

# Calculate and print the length of a square of area 10.
area = 10
length = math.sqrt(area)
print("The length of a square with area 10 is " + str(length) + ".\n")

# Calculate and print the sine, cosine, and tangent of 21 degrees
print("The sine of 21 degrees is " + str(math.sin(math.radians(21))) + ".\n")
print("The cosine of 21 degrees is " + str(math.cos(math.radians(21))) + ".\n")
print("The tangent of 21 degrees is " + str(math.tan(math.radians(21))) + ".\n")
